    private void BotonBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBusquedaActionPerformed
        String ide = BusquedaIDbox.getText().trim();
        
        int ident = Integer.parseInt(ide);
        boolean est;
        BusquedaUsuario search = new BusquedaUsuario();
        
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/dulceria","root","");
            PreparedStatement select = cn.prepareStatement("select * from usuario where ID = ?");
            
            select.setInt(1, ident);
            
            ResultSet rs = select.executeQuery();
            
            if(rs.next()){
                
                search.setVisible(true);  
                BusquedaIDbox.setText("");
                
                BusquedaUsuario.idbox.setText(rs.getString("ID"));
                BusquedaUsuario.nombrebox.setText(rs.getString("NOMBRE"));
                BusquedaUsuario.contrabox.setText(rs.getString("CONTRASEÑA"));   //if(ident equals 
                BusquedaUsuario.puestobox.setText(rs.getString("PUESTO"));
                BusquedaUsuario.sueldobox.setText(rs.getString("SUELDO"));
                BusquedaUsuario.celularbox.setText(rs.getString("CELULAR"));
                
                est = rs.getBoolean("ESTADO"); 
                if(est == true)
                    BusquedaUsuario.estbox.setText("Activo");
                else
                    BusquedaUsuario.estbox.setText("Inactivo");
                    
                
            }else{
                JOptionPane.showMessageDialog(null, "Usuario no encontrado");
            }
            
        }catch(Exception e){
            System.err.print("Error: " + e);
        }
        
        
    }//GEN-LAST:event_BotonBusquedaActionPerformed

