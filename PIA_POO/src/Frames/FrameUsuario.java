    private void BusquedaIDboxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BusquedaIDboxKeyTyped
        char validar = evt.getKeyChar();
        
        if(Character.isLetter(validar)){
            
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(rootPane,"Solo se pueden ingresar valores numericos");
        }
    }//GEN-LAST:event_BusquedaIDboxKeyTyped

