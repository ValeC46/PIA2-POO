    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed

        String id = idbox.getText().trim();
        int ide = Integer.parseInt(id);
        
        user.Eliminar(ide);

        this.setVisible(false);
        
        idbox.setText("");
        nombrebox.setText("");
        contrabox.setText("");
        puestobox.setText("");
        sueldobox.setText("");
        celularbox.setText("");
        estbox.setText("");
    }//GEN-LAST:event_EliminarActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        
        String id = idbox.getText().trim();
        int ide = Integer.parseInt(id);
        
        String nombre = nombrebox.getText().trim();
        String contraseña = contrabox.getText().trim();
        String puesto = puestobox.getText().trim();
        String sueldo = sueldobox.getText().trim();
        String celular = celularbox.getText().trim();
        String estado = estbox.getText().trim();
                
        user.Editar(ide, nombre, contraseña, puesto, sueldo, celular, estado);
        
        this.setVisible(false);
        
        idbox.setText("");
        nombrebox.setText("");
        contrabox.setText("");
        puestobox.setText("");
        sueldobox.setText("");
        celularbox.setText("");
        estbox.setText("");
    }//GEN-LAST:event_guardarActionPerformed

