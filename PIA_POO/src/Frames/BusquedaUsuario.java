    private void sueldoboxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sueldoboxKeyTyped
        char validar = evt.getKeyChar();
        
        if(Character.isLetter(validar)){
            
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(rootPane,"Solo se pueden ingresar valores numericos");
        }
    }//GEN-LAST:event_sueldoboxKeyTyped

