    private void ValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValidarActionPerformed
        int id = Integer.valueOf(idbox.getText().trim());
        
        if(vta.ValidarID(id)){
            try{
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/dulceria","root","");
                PreparedStatement select = cn.prepareStatement("select * from clientes where id = ?");

                select.setInt(1, id);

                ResultSet rs = select.executeQuery();

                if(rs.next()){

                    contact.setVisible(true);  
                    contactobox.setVisible(true);contactobox.setEditable(false);
                    nombret.setVisible(true);
                    nombrebox.setVisible(true);nombrebox.setEditable(false);
                    
                    Nuevo.setVisible(false);
                    Validar.setVisible(false);
                    idbox.setEditable(false);
                    
                    v2 = true;

                    nombrebox.setText(rs.getString("nombre"));
                    cliente = nombrebox.getText();
                    contactobox.setText(rs.getString("telefono"));    

                }else{
                    JOptionPane.showMessageDialog(null, "Usuario no encontrado");
                }

            }catch(Exception e){
                System.err.print("Error: " + e);
            }
            
        }else{
            idbox.setText("");
        }
        
    }//GEN-LAST:event_ValidarActionPerformed

    private void claveboxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_claveboxKeyTyped
        char validar = evt.getKeyChar();
        
        if(Character.isLetter(validar)){
            
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(rootPane,"Solo se pueden ingresar valores numericos");
        }
    }//GEN-LAST:event_claveboxKeyTyped

