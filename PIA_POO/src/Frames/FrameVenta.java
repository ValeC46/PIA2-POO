    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        
        int clave = Integer.parseInt(clavebox.getText().trim());
        int cantidad = Integer.parseInt(cantbox.getText().trim());
        
        String c1=clavebox.getText().trim();
        String c2=cantbox.getText().trim();
        
        if(c1.length()==0 ||c2.length()==0){
            JOptionPane.showMessageDialog(null, "Estos campos no pueden estar vacios");
            clavebox.setText(null);
            cantbox.setText(null);
        }else{
           
            if(vta.ValidadProducto(clave)){

                if(vta.ValidarEstado(clave)){
                   if(vta.ValidarCantidad(clave, cantidad)){
                       //CODIGO

                       int cant = vta.ObtenerCant(clave);

                       int cantnueva = cant - cantidad;
                       vta.ActualizarInventario(clave, cantnueva);

                       String producto = vta.Producto(clave);
                       float precio = vta.Precio(clave) * cantidad;
                       
                       v1 = true;

                       total = total + precio;
                       
                       totalbox.setText(Float.toString(total) );

                       Lista.append("\n" + producto + "  x" + cantidad + "  : $" + precio);

                       clavebox.setText(null);
                       cantbox.setText(null);

                    }else{
                        JOptionPane.showMessageDialog(null, "Cantidad Invalida");

                        cantbox.setText(null);
                    } 
                }
                else{
                    JOptionPane.showMessageDialog(null, "Producto no esta en venta");
                    clavebox.setText(null);
                    cantbox.setText(null);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Producto no encontrado");
                clavebox.setText(null);
                cantbox.setText(null);
            }

            Confirmar.setVisible(true);
        }
    }//GEN-LAST:event_AgregarActionPerformed

    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
       
        //SI YA SE COMPRO MINIMO UN ARTICULO Y SE INGRESO UN CLIENTE, YA ES POSIBLE CONFIRMAR LA COMPRA
        if(v1 && v2){
            
            float ingreso = Float.parseFloat(conbox.getText().trim());
            
            if(ingreso > total){
                
                float devolucion = ingreso - total;
                
                int randomid;
                
                do{
                    randomid = (int) Math.floor(Math.random()*(5000-1000+1)+1000);
                    
                }while(!vta.ValidarClave(randomid));
                
                
                String clientecompra = nombrebox.getText().trim();
                
                Texto = ("CLAVE DE COMPRA: " + randomid + "\n" + vta.timeStamp + "\nCLIENTE: " + clientecompra + "\n" + Lista.getText().replace("\n", "\r\n") + "\n\n" + "TOTAL: $" + total + "\nPAGADO CON: $" + ingreso + "\nDEVOLUCION: $" + devolucion);
                
                Ticket ver = new Ticket();
                ver.setVisible(true);
                this.setVisible(false);
                ver.lista.setText(Texto);
                
            }else{
                JOptionPane.showMessageDialog(null, "El monto a pagar es mayor");
                conbox.setText("");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos y hacer minimo una compra");
        }
        
        
    }//GEN-LAST:event_ConfirmarActionPerformed

